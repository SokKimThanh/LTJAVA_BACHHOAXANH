    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMaSV.getText() == "" || txtTenSV.getText() == "" || txtTuoi.getText() == "") {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin", "Warning", 1);
        } else {
            int masv = Integer.parseInt(txtMaSV.getText());
            String tensv = txtTenSV.getText();
            int tuoi = Integer.parseInt(txtTuoi.getText());
            int result = JOptionPane.showConfirmDialog(rootPane, "Đồng ý xóa sinh viên mã số " + masv);
            if (result == 0) {
                int status = lSinhVien.XoaDL(new SinhVien(masv, tensv, tuoi));
                if (status > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thông tin thành công", "Remove", 1);
                }
            }

            txtMaSV.setText("");
            txtTenSV.setText("");
            txtTuoi.setText("");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtMaSV.getText() == "" || txtTenSV.getText() == "" || txtTuoi.getText() == "") {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin", "Warning", 1);
        } else {
            int masv = Integer.parseInt(txtMaSV.getText());
            String tensv = txtTenSV.getText();
            int tuoi = Integer.parseInt(txtTuoi.getText());

            int status = lSinhVien.SuaDL(new SinhVien(masv, tensv, tuoi));
            if (status > 0) {
                JOptionPane.showMessageDialog(rootPane, "Sửa thông tin thành công", "Fixing", 3);
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

