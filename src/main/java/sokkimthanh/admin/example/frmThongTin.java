    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtMaSV.getText() == "" || txtTenSV.getText() == "" || txtTuoi.getText() == "") {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin", "Warning", 1);
        } else {
            int masv = Integer.parseInt(txtMaSV.getText());
            String tensv = txtTenSV.getText();
            int tuoi = Integer.parseInt(txtTuoi.getText());

            int status = lSinhVien.ThemDL(new SinhVien(masv, tensv, tuoi));
            txtMaSV.setText("");
            txtTenSV.setText("");
            txtTuoi.setText("");
            if (status > 0) {
                JOptionPane.showMessageDialog(rootPane, "Thêm thông tin thành công", "Adding", 2);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

