    private void Hide() {
        //Set null texts
        txtMuaHang_MaSP.setText(null);
        txtMuaHang_TenSP.setText(null);
        txtMuaHang_GiaBan.setText(null);
        txtMuaHang_SoLuong.setText(null);
        cbbMuaHang_Loai.setSelectedIndex(0);
        sldMuaHang_SoLuong.setValue(0);

        //Deactivate enable
        txtMuaHang_MaSP.setEnabled(false);
        txtMuaHang_TenSP.setEnabled(false);
        txtMuaHang_GiaBan.setEnabled(false);
        txtMuaHang_SoLuong.setEnabled(false);
        cbbMuaHang_Loai.setEnabled(false);
        sldMuaHang_SoLuong.setEnabled(false);
    }

    private void ShowTable() {
        DefaultTableModel model = (DefaultTableModel) tblMuaHang_DanhSach.getModel();
        model.setRowCount(0);

        //doi int loaisanpham sang string loaisanpham
        for (SanPham sanPham : listSP.getData()) {
            String loai = "";
            switch (sanPham.getLoaiSP()) {
                case 0:
                    loai = "Thit, ca, trung";
                    break;
                case 1:
                    loai = "Rau, cu, qua, nam";
                    break;
                default:
                    loai = "Nuoc, sua";
                    break;
            }
            Object[] rowData = new Object[]{sanPham.getMaSP(), sanPham.getTenSP(), loai, sanPham.getGiaSP(), sanPham.getTonKho()};
            model.addRow(rowData);
        }
        listSP.XuatDS();
    }

