    private void tabMainStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tabMainStateChanged
        // TODO add your handling code here:
        if (tabMuaHang.isShowing()) {
            cbbMuaHang_TimKiem.setSelectedIndex(0);
            this.timKiem = 0;
            Hide();
            txtMuaHang_MaSP.setEnabled(true);
            listSP.DocDL();

//            DefaultTableCellRenderer centerCell = new DefaultTableCellHeaderRenderer();
            DefaultTableCellRenderer centerCell = new DefaultTableCellRenderer();
            centerCell.setHorizontalAlignment(JLabel.CENTER);
            for (int i = 0; i < tblMuaHang_DanhSach.getColumnCount(); i++) {
                tblMuaHang_DanhSach.getColumnModel().getColumn(i).setCellRenderer(centerCell);
            }
            ShowTable();
        }
    }//GEN-LAST:event_tabMainStateChanged

    private void cbbMuaHang_TimKiemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbMuaHang_TimKiemItemStateChanged
        // TODO add your handling code here:
        timKiem = cbbMuaHang_TimKiem.getSelectedIndex();
        Hide();
        switch (timKiem) {
            case 0:
                txtMuaHang_MaSP.setEnabled(true);
                txtMuaHang_MaSP.requestFocus();
                break;
            case 1:
                txtMuaHang_TenSP.setEnabled(true);
                txtMuaHang_TenSP.requestFocus();
                break;
            case 2:
                cbbMuaHang_Loai.setEnabled(true);
                break;
            case 3:
                txtMuaHang_GiaBan.setEnabled(true);
                txtMuaHang_GiaBan.requestFocus();
                break;
            default:
                txtMuaHang_SoLuong.setEnabled(true);
                txtMuaHang_SoLuong.requestFocus();
                sldMuaHang_SoLuong.setEnabled(true);
                break;
        }
        System.out.println(timKiem);
    }//GEN-LAST:event_cbbMuaHang_TimKiemItemStateChanged

