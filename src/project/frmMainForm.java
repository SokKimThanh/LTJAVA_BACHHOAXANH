    private void txtMuaHang_SoLuongKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMuaHang_SoLuongKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                int soLuong = Integer.parseInt(txtMuaHang_SoLuong.getText());
                if (soLuong >= 0 && soLuong <= 1000) {
                    sldMuaHang_SoLuong.setValue(soLuong);
                } else {
                    throw new NumberFormatException();
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Số lượng sản phẩm phải là số nguyên từ 0 - 1000");
                txtMuaHang_SoLuong.setText("0");
                sldMuaHang_SoLuong.setValue(0);
            }
        }
    }//GEN-LAST:event_txtMuaHang_SoLuongKeyPressed

